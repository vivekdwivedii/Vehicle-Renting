    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        // Code for change password
        try{
            String opswd=new String(jPasswordField1.getPassword());
            String npswd=new String(jPasswordField2.getPassword());
            String cpswd=new String(jPasswordField3.getPassword());
            DBCon db=new DBCon();
            db.pstmt=db.con.prepareStatement("select password from login where user=?");
            db.pstmt.setString(1, user);
            db.rst=db.pstmt.executeQuery();
            if(db.rst.next())
            {
                String dbpswd=db.rst.getString(1);
                if(opswd.equals(dbpswd)){
                    if(npswd.equals(cpswd)){
                        db.pstmt=db.con.prepareStatement("Update login set password=? where user=?");
                        db.pstmt.setString(1,npswd);
                        db.pstmt.setString(2, user);
                        int i=db.pstmt.executeUpdate();
                        if(i>0){
                            JOptionPane.showMessageDialog(this,"Password Updated Successfully");
                            ChangePswd.setVisible(false);
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(this,"New and Confirm Password does not Match");
                    }
                    
                }
                else{
                    JOptionPane.showMessageDialog(this,opswd+" Old Password does not Match ");
                }
              
                
            }
            else{
                JOptionPane.showMessageDialog(this,"Plase Login First !!!!");
            }
            
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton12ActionPerformed

